package variables;

public class Casting {
    public static void main(String[] args)
    {
        double kdetKo;
        kdetKo = 2.4f;

        //casting premetive datatype
        int  kdetChma ;
        kdetChma = (int)kdetKo;

        System.out.println(kdetKo);
        //casting kdetKo ->  to integer assigned to -> kdetChma
        System.out.println(kdetChma);

    }

}
